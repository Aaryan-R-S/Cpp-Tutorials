#include<iostream>
using namespace std;

int main(){

    for (int i = 0; i < 4; i++)
    {
        cout<<i<<endl;
    }

    int i = 0;
    while (i<4)
    {
        cout<<i<<endl;
        i++;
    }

    int j = 0;
    do
    {
        cout<<j<<endl;
        j++;
    } while (j>4);
    
    
    return 0;
}